export const getStaticTranslation = (text, langCode) => {
    if (!text || langCode === 'en') return text;

    const entry = STATIC_TRANSLATIONS[text];
    if (entry && entry[langCode]) {
        return entry[langCode];
    }

    // Case insensitive fallback
    const lowerKey = Object.keys(STATIC_TRANSLATIONS).find(k => k.toLowerCase() === text.toLowerCase());
    if (lowerKey && STATIC_TRANSLATIONS[lowerKey][langCode]) {
        return STATIC_TRANSLATIONS[lowerKey][langCode];
    }

    return null;
};
